	// Add user logic here
integer pwm_current_count;
integer pwm_period = 100;//default period to 100
integer pwm_duty_period = 50;//default duty to 50

reg led_state = 0;
assign LED_signal = led_state;

always @(posedge s00_axi_aclk)begin
    //Check if data on bus is valid
    if(s00_axi_wvalid) begin
        //Writing to PWM period
        if(s00_axi_awaddr==0)begin
            //Since data is being read, will signal for tready to wait until one repeat  
            pwm_period = s00_axi_wdata;
        end
        //Writing to PWM duty
        else if(s00_axi_awaddr==4)begin
            pwm_duty_period = s00_axi_wdata;
        end
    end
    
    else begin
        //High side of pwm
        if(pwm_current_count < pwm_duty_period)begin
            led_state = 1;
        end
        //Low side of pwm
        else begin
            led_state = 0;
            
            if(pwm_current_count > pwm_period)begin
                pwm_current_count = 0;
            end
        end
        pwm_current_count=pwm_current_count+1;
    end
end
	// User logic ends

